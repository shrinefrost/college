#program to show case use of comparison operator and logical operations


x=10
y=30


#comparision operator

print(f"x==y:  {x==y}") 
print(f"x!=y:  {x!=y} ") 
print(f"x>=y:  {x>=y} ") 
print(f"x<=y:  {x<=y}")  
print(f"x>y:  {x>y} ") 
print(f"x<y:   {x<y}") 


#logical operation 

a=True
b=False

print(f"a and b: {a and b}")
print(f"a or b: {a or b}")
print(f" not a: {not a}")